module edge_detector_locked(en,
    im11,
    im21,
    im31,
    im12,
    im22,
    im32,
    im13,
    im23,
    im33,
    dxy,
    ready,
    key
);
    input en;

    input [15:0] im11;
    input [15:0] im12;
    input [15:0] im13;
    input [15:0] im21;
    input [15:0] im22;
    input [15:0] im23;
    input [15:0] im31;
    input [15:0] im32;
    input [15:0] im33;

    input [23:0] key;

    output reg [7:0] dxy;
    output reg ready;

    reg [15:0] dx;
    reg [15:0] dy;
    reg [15:0] dx_a, dx_b;
    reg [15:0] dy_a, dy_b;
    
    always @(posedge en) begin
        ready <= 1'b0;
        
        // Horizontal gradient ----------------------------------------------------
        dx_a <= (
            (im11 ^ ((key[0] ? (16'h1 << 7) : 16'h0) | (~key[1] ? (16'h1 << 3) : 16'h0))) +
            ((im21 ^ (~key[2] ? (16'h1 << 8) : 16'h0)) << 1) +
            im31
        );

        dx_b <= (
            (im13 ^ ((~key[3] ? (16'h1 << 8) : 16'h0) | (~key[4] ? (16'h1 << 11) : 16'h0))) +
            (((im23 ^ ((~key[5] ? 16'h1 : 16'h0) | (~key[6] ? (16'h1 << 14) : 16'h0)))) << 1) +
            (im33 ^ (key[7] ? (16'h1 << 6) : 16'h0))
        );

        if (dx_a >= dx_b)
            dx <= (dx_a - dx_b) ^
                ((~key[19] ? (16'h1 << 14) : 16'h0) |
                    (key[20]  ? (16'h1 << 1)  : 16'h0) |
                    (key[21]  ? (16'h1 << 8)  : 16'h0));
        else
            dx <= (dx_b - dx_a) ^
                ((key[17]  ? (16'h1 << 9)  : 16'h0) |
                    (~key[18] ? (16'h1 << 5)  : 16'h0));
        // -------------------------------------------------------------------------

        // Vertical gradient -------------------------------------------------------
        dy_a <= (
            (im31 ^ ((key[8] ? (16'h1 << 7) : 16'h0) | (~key[9] ? (16'h1 << 3) : 16'h0))) +
            ((im32 ^ (~key[10] ? (16'h1 << 8) : 16'h0)) << 1) +
            (im33 ^ (key[16] ? (16'h1 << 15) : 16'h0))
        );

        dy_b <= (
            (im11 ^ ((~key[11] ? (16'h1 << 8) : 16'h0) | (~key[12] ? (16'h1 << 11) : 16'h0))) +
            (((im12 ^ ((~key[13] ? 16'h1 : 16'h0) | (~key[14] ? (16'h1 << 14) : 16'h0)))) << 1) +
            (im13 ^ (key[15] ? (16'h1 << 6) : 16'h0))
        );

        if (dy_a >= dy_b)
            dy <= (dy_a - dy_b);
        else
            dy <= (dy_b - dy_a) ^ (key[23] ? (16'h1 << 10) : 16'h0);
        // --------------------------------------------------------------------------
        
        dxy <= ((dx + dy) >= 16'd255) ? 8'd255 : 8'd0;
        ready <= 1'b1;
    end
endmodule
